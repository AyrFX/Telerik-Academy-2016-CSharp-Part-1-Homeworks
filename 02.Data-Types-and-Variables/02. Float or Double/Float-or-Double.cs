using System;

class FloatOrDouble
{
    static void Main()
    {
        double doubleNumber = 34.567839023D;
        Console.WriteLine(doubleNumber);
        float floatNumber = 12.345F;
        Console.WriteLine(floatNumber);
        doubleNumber = 8923.1234857F;
        Console.WriteLine(doubleNumber);
        floatNumber = 3456.091F;
        Console.WriteLine(floatNumber);
    }
}